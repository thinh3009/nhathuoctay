function createProduct(seed: ProductSeed): Product {
  const reviews = seed.reviews ?? createReviews(seed.name)

  return {
    ...seed,
    defaultQuantity: 1,
    images: seed.imageLabels ?? [
      'Chính diện',
      'Mặt bên',
      'Thành phần',
      'Hướng dẫn dùng',
      'Bao bì',
    ],
    rating: seed.rating ?? 4.8,
    reviewCount: seed.reviewCount ?? 10,
    commentCount: seed.commentCount ?? 18,
    reviews,
  }
}

